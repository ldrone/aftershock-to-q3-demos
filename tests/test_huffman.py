def test_huffman_encode(huffman):
    assert huffman.encode(111) == bitarray('1100 1101')
    assert huffman.encode(256) is None


def test_huffman_decode(huffman):
    assert huffman.decode(bitarray('11001101')) == (111, 8)
    assert huffman.decode(bitarray('11001101 11001101')) == (111, 8)
    assert huffman.decode(bitarray('11010011 01')) == (89, 10)
    assert huffman.decode(bitarray('1')) is None


